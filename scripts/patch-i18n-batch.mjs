/** Deep-merge src into dest in-place. */
function merge(dest, src) {
  for (const [k, v] of Object.entries(src)) {
    if (v && typeof v === "object" && !Array.isArray(v) && dest[k] && typeof dest[k] === "object") {
      merge(dest[k], v);
    } else {
      dest[k] = v;
    }
  }
  return dest;
}

