function merge(base, patch) {
  for (const k of Object.keys(patch)) {
    if (typeof patch[k] === "object" && patch[k] !== null && !Array.isArray(patch[k])) {
      base[k] = base[k] && typeof base[k] === "object" ? base[k] : {};
      merge(base[k], patch[k]);
    } else {
      base[k] = patch[k];
    }
  }
  return base;
}

